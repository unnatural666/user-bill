              （例如：冻结1000元保证金，规定时间内未完成支付、背书、票据签收等操作，则扣除冻结比例10%，即：100元）

              14.2 强制取消的扣除: 出现以下情形，将强制扣除违约一方的保证金，扣除的保证金将全部赔付给守约一方：1）双方就取消订单不能达成一致，而申请方强制取消订单的，则扣除其在该订单上所交的剩余未被扣除的保证金；2）双方就取消订单不能达成一致且平台判定为不能取消后, 申请方强制取消订单的，则扣除其在该订单上所交的剩余未被扣除的保证金；

