              1)转让方与受让方的转、受让行为已完成的；

              2)转让方与受让方任意一方申请解除，并经相对方同意后协商一致解除合同的；

              3)转让方与受让方任意一方申请解除，且无法与相对方协商一致，经平台判定符合解除条件的。

              13.2转让方的保证金自动解除冻结规则：转让方把处于“匹配中”的票据转为“暂停匹配”，转让方冻结的保证金实时解冻。

              13.3受让方的保证金自动解除冻结规则：受让方对订单签约确认后,转让方不接受报价等原因放弃订单, 此时受让方冻结的保证金实时解冻。



              第十四条 冻结保证金的扣除规则

