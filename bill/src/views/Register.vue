              19.6 双方对订单确认完成的，系统通知受让方支付对价。受让方将相应款项支付至受让方在存管银行华夏银行股份有限公司自行开设的银行二类账户，商票圈平台委托银行实时冻结资金。受让方应在系统要求时间内完成该笔订单对价的支付，超时支付的将按照保证金规则扣除一定比例的保证金。受让方不按照系统通知支付相应对价的，进入取消流程；

              19.7 受让方支付完成并到账后，系统通知转让方背书。转让方应在系统要求时间内将订单指定票据的收益权，以票据背书的方式转让于受让方名下。转让方应在系统要求时间内完成背书，超时完成的将按照保证金规则扣除一定比例的保证金。转让方不按照系统通知完成背书的，进入取消流程；

              19.8转让方通过网上银行完成背书流程后，通过系统通知受让方签收背书。受让方应在系统要求时间内完成签收背书，超时完成的将按照保证金规则扣除一定比例的保证金。受让方未收到票据签收提示的，可以在系统中提示转让方背书；受让方拒绝签收票据的，进入取消流程；

              19.9 受让方签收完成，系统向存管银行发送指令，由银行将支付对价转入转让方在存管银行自行开设的银行二类账户内。系统通知双方订单已完成，已冻结的保证金实时解冻；

