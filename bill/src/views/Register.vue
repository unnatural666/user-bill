              第三章 保证金规则



              第十一条 为保证 企业间票据撮合平台会员双方的权益，平台将为转让方与受让方开设保证金账户专户，各方在发起转、受让操作之前，应当充值一定比例的金额作为保证金；该保证金将在各方自有的二类账户进行冻结保管，平台不接受任何一方的保管委托。各方同意按照本章节的规定执行保证金规则。



              第十二条 保证金的冻结

              12.1平台会员无论作为转让方或者受让方，为保障双方在订单操作过程中不会被无故取消或延期操作等权益，双方在商票圈发布票据或受让票据时，需以票面金额为基数按照一定比例冻结保证金。目前保证金冻结比例为2‰（千分之二），冻结的保证金不少于人民币贰佰元，商票圈平台可对保证金的金额动态调整。

              12.2保证金账户资金分为可用保证金与冻结保证金两部分，可用保证金可随时充值或提现；保证金按照本规则进行冻结后划入冻结保证金账户，解冻后划拨至可用保证金内，如有违约责任，有可能会扣除冻结的保证金部分或全部金额。

              12.3转让方的保证金冻结规则：a)转让方向平台上传票面信息完成后，点击“一键融资”进入匹配中，系统自动冻结保证金；b)转让方在未匹配票据列表中，点击“一键融资”进入匹配中，系统自动冻结保证金。

              12.4受让方的保证金冻结规则：受让方对转让方提交的票面信息及报价确认无误，与转让方签署电子合同前，系统自动冻结保证金。



              第十三条 保证金的冻结解除

              13.1条：转让方与受让方保证金的自动解除冻结

