              第二十四条 平台经纪商分为个人经纪商和企业经纪商。



              第二十四条 成为经纪商会员需先行完成以下操作：

              24.1经纪商需在平台完成注册并在平台开通“钱包账号”；

              24.2经纪商通过平台生成的推荐链接或经纪商会员专属推荐码向平台推荐客户。



              第二十五条 奖励规则

              25.1 由经纪商会员推荐的双方在平台完成的每一笔订单，平台均向经纪商会员计提奖励佣金。奖励标准为按照票面金额的一定比例进行计算；

              25.2 平台将依据订单中票据承兑人的信用评级、成交金额等因素对奖励佣金作出调整，并在订单详情中列明。



              第二十六条 结算规则

          企业间票据撮合平台按照自然月度向经纪商结算奖励佣金，经纪商会员可以在“我的钱包”查看奖励明细。平台于次月10日内对上一个月度的奖励佣金进行结算，并划转至经纪商会员在平台开设的“钱包账户”内。经纪商会员可自行通过平台绑定一般银行账户对钱包内的资金进行提现。



              第二十七条 个人经纪商根据本规则所获得的奖励佣金，按照国家法律法规应缴纳的税费由平台进行代扣代缴。企业经纪商根据本规则获得的奖励佣金，应向平台开具增值税专用发票。



