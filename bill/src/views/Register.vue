              19.3 系统匹配成功，实时通知票据受让方确认是否接受该笔订单。受让方在接受订单前，应保证账户内有足够的资金作为保证金。受让方经审查对转让方提交的票面信息及报价确认无误并选择接受的，按照系统的提示进行签约确认。受让方经审查选择不接受的，由系统另行匹配；

