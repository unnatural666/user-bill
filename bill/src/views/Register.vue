              第二十条 取消流程

              20.1 任意一方在订单匹配并确认完成后申请取消订单的，应向相对方提交取消的原因及证据；相对方同意取消订单的，订单结束；相对方不同意取消订单的，应提交拒绝的原因及证据；

              20.2 相对方拒绝取消订单申请的，申请一方可强制取消订单，但应按照本规则第三章向相对方赔付保证金；申请一方也可以在收到相对方的拒绝后，向平台进行申诉，由平台根据各方提交的书面材料及陈述进行判定；

              20.3 平台经审核后，认为符合取消理由的，订单取消，流程结束。认为不符合取消理由的，申请方可以选择继续履行，也可以强制取消订单，如强制取消的并按照本规则第三章向相对方赔付保证金；

              20.4订单进入“待签收”状态，任意一方均不可申请强制取消。



              第二十一条 技术服务费

              21.1 企业间票据撮合平台向会员提供的服务付出了大量的成本，会员同意并认可 企业间票据撮合平台服务费收费标准的计算规则为：转让方实收金额（成交金额）=受让方实扣金额-技术服务费[成交金额*（基本费率±动态系数）]。商票圈平台为提高转让方与受让方之间的信息撮合效率，在系统中设置了基本费率，该费率标准会根据票据的票面金额、承兑主体信用、票据剩余天数以及市场供需等因素动态调整并自动生成；

              21.2具体转让金额、技术服务费等以单笔订单内容为准。



              第二十二条  企业间票据撮合平台保留调整技术服务费定义及技术服务费金额计算方法的最终解释权。







